public class DataTypes {
    public static void main(String[] args) {
        //Integer Data type
        byte num1 = 100;
        short num2 = 1000;
        int num3 =3456665;
        long num4 = 43577646466L;

        //Floating point data type
        float num5=123.544f;
        double num6=345566.445d;

        //Boolean data type
        boolean redLightSwitch = false;
        boolean blackLightSwitch = true;
        //Character data type
        char char1='%';
        char char2='y';
        char char3='0';
        char char4='\n';

        //String
        String text1="This is very good text";

        System.out.println(num1);
        System.out.println(num6);
        System.out.println(redLightSwitch);
        System.out.println(blackLightSwitch);
        System.out.println(char4);
        System.out.println(char2);
        System.out.println(text1);
    }
}
